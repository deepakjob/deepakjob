using Microsoft.EntityFrameworkCore;
using TestApiProject;
using TestApiProject.DBContext;
using TestApiProject.RepositoryService;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddDbContext<ProductDbContext>(
        options => options.UseSqlServer("name=ConnectionStrings:ProductDB"));
builder.Services.AddTransient(typeof(IProduct), typeof(ProductService));
var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
